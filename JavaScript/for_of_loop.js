let str = "JavaScript";
let size = 0;
// For_of Loop is used to itterate the some data-types like String and arrays. (Itterate means to apply loops)
for (let i of str){
    console.log("i=",i);
    size++;
}

console.log("String size = ", size);

/* The line `console.log("String size = ", size);` is outputting the size of the string `JavaScript` to
the console. It will display the message "String size = " followed by the value of the `size`
variable, which represents the length of the string `JavaScript`. */


